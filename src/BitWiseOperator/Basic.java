package BitWiseOperator;

public class Basic {

    public static void main(String[] args) {
        System.out.println(4 & 5);

        System.out.println(5 | 6);

        System.out.println(5 ^ 6);

        System.out.println(~5);

        System.out.println(5 << 2);

        System.out.println( 6 >> 1 );


    }
}
