package Operator;

public class LogicalOperator {
    public static void main(String[] args) {

        //   && operator

        System.out.println((3>2) && (5>0));
        System.out.println((3<2) && (5>0));

        //    ||  operator

        System.out.println((3<2) || (5>0));
        System.out.println((3>2) && (5<0));



    }
}
