package ConditionalStatement;

public class Switch {
    public static void main(String[] args) {
        int no =2;
        switch (no){
            case 1 :
                System.out.println("no 1");
                break;
            case 2 :
                System.out.println("no 2");
                break;
            default:
                System.out.println("wrong number");
        }
    }
}
